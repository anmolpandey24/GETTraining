package UniversityExamManagementSystem;

public class Student {
	String studendID;
	String name;
	int attendper;
	public Student() {
		super();
	}
	public Student(String studendID, String name, int attendper) {
		super();
		this.studendID = studendID;
		this.name = name;
		this.attendper = attendper;
	}
	

}
